class ProxyMiddleware(object):
    def __init__(self):
        self.server = settings['MONGODB_SERVER']
        self.port = settings['MONGODB_PORT']
        self.db = settings['MONGODB_DB']
        self.col = settings['MONGODB_COLLECTION']
        self.proxy_db = settings['PROXY_DB']
        self.proxy_col = settings['PROXY_COLLECTION']
        connection = MongoClient(self.server, self.port)
        db = connection[self.db]
        proxy_db=connection[self.proxy_db]
        self.collection = db[self.col]
        self.proxy_collection = proxy_db[self.proxy_col]
    def get_proxy(self):
        p_list=self.proxy_collection.find().sort("verify_time", DESCENDING)
        self.proxy_list=[]
        proxy={}
